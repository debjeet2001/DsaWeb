import java.util.*;

public class IntegerMaxMin{

    public static void main(String[] args){
        int min = Integer.MIN_VALUE;
        int max = Integer.MAX_VALUE;
        System.out.println(min + " " + max);

        int a  = 'a' + 3;
        System.out.println(a);

        char b = 100;
        System.out.println(b);
    }
}